import numpy as np

# Creating a numpy Arrays
# 1D -> These are vectors
a=np.array([1,2,3])
print(a)


# 2D -> These are matrix
b=np.array([[1,2,3],
            [6,7,8],
            [-1,2,9]])
print(b)

# 3D -> These are tensor
c=np.array([[
    [1,2,3],
    [4,5,0]],
    [
    [-1,-3,4],
    [5,9,1]
    ]
    ]
    )
print(c)